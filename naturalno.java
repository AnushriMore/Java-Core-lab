//Write a Java program to display the first 10 natural numbers.
package demo; // Define the package name
 
// Create a class named naturalno
public class naturalno {
    // Main method: Entry point of the program
    public static void main(String[] args) {
        int i; // Declare a variable 'i' to iterate over natural numbers

        // Print a message indicating the first 10 natural numbers
        System.out.println("The first 10 natural numbers are:\n");

        // Use a for loop to iterate from 1 to 10 and print each natural number
        for (i = 1; i <= 10; i++) {
            System.out.println(i); // Print the current value of 'i'
        }

        System.out.println("\n"); // Print an empty line for spacing
    }
}
